class WrapperClass {
    // Wrapper Class: A class that converts primitive types (like int) into objects
    // (like Integer).
    // Autoboxing: Automatically converting a primitive to its wrapper object.
    // Unboxing: Automatically converting a wrapper object back to primitive.

    public static void main(String[] args) {
        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Sanjukta";

        if (b == '@') {
            System.out.println("This is true");
        }
    }
}
